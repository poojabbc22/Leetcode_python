if __name__ == '__main__':
    s = input()
    if s.isalnum():
        print(True) 
    if s.isalnum():
        print(True)
    if s.isalpha():
        print(True)
    if s.isdigit():
        print(True)
    if s.islower():
        print(True)
    if s.isupper():
        print(True)
    else: 
        print(False)